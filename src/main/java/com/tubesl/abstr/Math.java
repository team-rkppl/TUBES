package com.tubesl.abstr;


/**
 *
 * @author Adhi
 */
public abstract class Math {

    /**
     * fungsi untuk melakukan modular
     * @param n1 int
     * @param n2 int
     */
    abstract public int mod(int n1, int n2);

    /**
     * fungsi untuk melakukan sqrt dari kelas Math
     * @param n double
     */
    abstract public double sqrt(double n);

}
